'''
Exemplo de utilização do laço for
'''

#este laço exibe até o valor 9 
for i in range(1,10):
    print("Número: ",i)

print("")

#variação do laço for, que exibe a cada 2 números
for i in range(1,10,2):
    print("Número: ",i)

print("")


#variação do laço for, que decrementa o valor inicial
for i in range(10,1,-2):
    print("Número: ",i)