'''
Exemplo de função simples em python sem retorno de valor
'''

#Definição da função soma utilizando a declaração (def)
def soma(a,b):
    resultado = a+b
    print("Resuldao de {0} + {1}: {2}".format(a,b,resultado))


#Variáveis de exemplo para soma
x = 10
y = 30

#Chamada a função soma()
soma(x,y)